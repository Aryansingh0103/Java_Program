class Weekno {
    public static void main(String argu[]) {
        int Week = 3;
        switch (Week) {
            case 1:
                System.out.println("Showing Week: Monday");
                break;
            case 2:
                System.out.println("Showing Week: Tuesday");
                break;
            case 3:
                System.out.println("Showing Week: Wednesday");
                break;
            case 4:
                System.out.println("Showing Week: Thursday");
                break;
            case 5:
                System.out.println("Showing Week: Friday");
                break;
            case 6:
                System.out.println("Showing Week: Saturday");
                break;
            case 7:
                System.out.println("Showing Week: Sunday");
                break;
            default:
                System.out.println("Invalid input - Wrong Week number.");
                break;
        }
    }
}
